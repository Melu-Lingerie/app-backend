package ru.melulingerie.domain;

/**
 * Defines the type of media content.
 * Used to distinguish between different kinds of media files
 * associated with a product, such as images or videos.
 */
public enum MediaType {
    /**
     * Represents an image file, like a JPEG or PNG.
     */
    IMAGE,
    /**
     * Represents a video file, like an MP4.
     */
    VIDEO
}